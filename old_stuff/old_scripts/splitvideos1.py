import cv2
print(cv2.__version__)

vidcap = cv2.VideoCapture()

folder_dir = 'data/'
output_dir = 'Images/NotCutting/'
print("output_dir: ", output_dir)

import os
print(os.listdir('.'))

# videos = os.listdir('hmdb51_org/' + folder_dir)
videos = ['s20-d07-cam-002_Trim.mp4']
print("Videos: ", videos)

imgs=os.listdir(output_dir)
print("Before: ", len(imgs))


# folder_name = ""
# try:
